//Tipos de datos numericos

let miEntero = 10;
console.log(miEntero);
console.log(typeof(miEntero));
console.log(typeof miEntero);

let miFlotante = 10.5;
console.log(miFlotante);
console.log(typeof(miFlotante));
console.log(typeof miFlotante);

//Tipos de datos string
let miCadena = "Hola";
console.log(miCadena);
console.log(typeof miCadena);

let miCadena2 = 'Hola';
console.log(miCadena2);

let miCadena3 = `Hola`;
console.log(miCadena3);


//Tipos de datos Boolean
let miBolean = true //true o false
console.log(miBolean);
console.log(typeof miBolean);

//Tipos de datos null
let miNull = null;
console.log(miNull);


//Tipos de datos undefined (ausencia de valor)
let miUndefined = undefined;
console.log(miUndefined);
console.log(typeof miUndefined);

